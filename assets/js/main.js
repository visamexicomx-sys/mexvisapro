function initScrollReveal() {
  const reveals = document.querySelectorAll('.reveal, .reveal-left, .reveal-right, .reveal-scale');
  if (!reveals.length) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('revealed');
      }
    });
  }, { threshold: 0.12, rootMargin: '0px 0px -40px 0px' });

  reveals.forEach(el => observer.observe(el));
}

function initCounterAnimation() {
  const counters = document.querySelectorAll('[data-count]');
  if (!counters.length) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting && !entry.target.dataset.counted) {
        entry.target.dataset.counted = 'true';
        animateCounter(entry.target);
      }
    });
  }, { threshold: 0.5 });

  counters.forEach(el => observer.observe(el));
}

function animateCounter(el) {
  const target = parseInt(el.dataset.count);
  const suffix = el.dataset.suffix || '';
  const prefix = el.dataset.prefix || '';
  const duration = 1618;
  const start = performance.now();

  function update(now) {
    const elapsed = now - start;
    const progress = Math.min(elapsed / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    const current = Math.floor(eased * target);

    el.textContent = prefix + current.toLocaleString() + suffix;

    if (progress < 1) requestAnimationFrame(update);
  }

  requestAnimationFrame(update);
}

function initParticles() {
  const container = document.querySelector('.hero-particles');
  if (!container) return;

  const PHI = 1.618;
  for (let i = 0; i < 34; i++) {
    const p = document.createElement('div');
    p.classList.add('particle');
    const size = Math.random() * 6.18 + 1.618;
    p.style.width = size + 'px';
    p.style.height = size + 'px';
    p.style.left = ((i * PHI * 100) % 100) + '%';
    p.style.animationDuration = (Math.random() * 16.18 + 10) + 's';
    p.style.animationDelay = (Math.random() * 6.18) + 's';
    p.style.opacity = (0.03 + Math.random() * 0.05).toFixed(3);
    container.appendChild(p);
  }
}

function initWhatsAppWidget() {
  const toggle = document.getElementById('waToggle');
  const chatBox = document.getElementById('waChatBox');
  const closeBtn = document.getElementById('waClose');
  const icon = document.getElementById('waIcon');
  if (!toggle || !chatBox) return;

  let isOpen = false;

  toggle.addEventListener('click', (e) => {
    e.preventDefault();
    isOpen = !isOpen;
    chatBox.classList.toggle('open', isOpen);
    icon.className = isOpen ? 'bi bi-x-lg' : 'bi bi-whatsapp';
  });

  if (closeBtn) {
    closeBtn.addEventListener('click', () => {
      isOpen = false;
      chatBox.classList.remove('open');
      icon.className = 'bi bi-whatsapp';
    });
  }

  document.addEventListener('click', (e) => {
    if (isOpen && !e.target.closest('.wa-widget')) {
      isOpen = false;
      chatBox.classList.remove('open');
      icon.className = 'bi bi-whatsapp';
    }
  });
}

function initActiveNav() {
  const sections = document.querySelectorAll('section[id]');
  const navLinks = document.querySelectorAll('.navbar-nav .nav-link');

  window.addEventListener('scroll', () => {
    let current = '';
    sections.forEach(section => {
      const top = section.offsetTop - 120;
      if (window.scrollY >= top) current = section.getAttribute('id');
    });

    navLinks.forEach(link => {
      link.classList.remove('active');
      if (link.getAttribute('href') === '#' + current) {
        link.classList.add('active');
      }
    });
  });
}

