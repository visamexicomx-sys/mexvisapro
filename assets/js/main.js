function detectLang() {
  var path = window.location.pathname;
  if (path.indexOf('/es/') !== -1 || path.indexOf('/es') === path.length - 3) return 'es';
  if (path.indexOf('/ru/') !== -1 || path.indexOf('/ru') === path.length - 3) return 'ru';
  if (path.indexOf('/zh/') !== -1 || path.indexOf('/zh') === path.length - 3) return 'zh';
  if (path.indexOf('/en/') !== -1 || path.indexOf('/en') === path.length - 3) return 'en';
  return localStorage.getItem('lang') || 'en';
}
let currentLang = detectLang();

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (el.dataset.count) {
        el.dataset.counted = '';
        animateCounter(el);
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });

  document.querySelectorAll('[data-wa-msg]').forEach(el => {
    const msgKey = el.getAttribute('data-wa-msg');
    if (translations[lang] && translations[lang][msgKey]) {
      el.href = 'https://wa.me/529987583392?text=' + encodeURIComponent(translations[lang][msgKey]);
    }
  });

  document.querySelectorAll('.lang-switcher .btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

